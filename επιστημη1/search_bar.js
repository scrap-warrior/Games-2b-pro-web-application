let score = 0; // Αρχικό σκορ
let currentQuestion = 1; //τρέχουσα ερώτηση
let guest_name = "";

function start_quiz() {
  document.getElementById("name").style.display = "none";
  document.getElementById("start_button").style.display = "none";
  document.getElementById("questions").style.display = "block";
  guest_name = document.getElementById("name").value; //το όνομα του παίκτη
  show_question(currentQuestion);
}

function answer_question(answer) {
  if (!guest_name) {
    guest_name = document.getElementById("name").value;
  }
  // Έλεγχος σωστής απάντησης
  if (answer === "correct") {
    score++;
  }

  // Μετάβαση στην επόμενη ερώτηση
  currentQuestion++;
  if (currentQuestion <= 6) {
    show_question(currentQuestion);
  } else {
    // Τέλος quiz
    alert("Τέλος του quiz, " + guest_name + "! Το σκορ σου είναι: " + score);
    document.getElementById("questions").style.display = "none";
  }
}

function show_question(questionNumber) {
  // Απόκρυψη των ερωτήσεων
  for (let i = 1; i <= 6; i++) {
    document.getElementById(`question_${i}`).style.display = "none";
  }
  // Εμφάνιση τρέχουσας ερώτησης
  document.getElementById(`question_${questionNumber}`).style.display = "block";
}


